package For;

public class Enteros {
    public static void main(String[] args) {
        int[] numeros  = {1,2,3,4,5};
        System.out.println("Los elementos del array son: ");
        for (int i=0; i<numeros.length; i++){
            System.out.println("Array["+i+"] = "+numeros[i]);
        }
    }
    
}
